    private void searchClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchClienteMouseClicked
        verificadorVazio = contadorVazioCpf(jFormattedTextPreencherCpfCliente.getText());
        
        if(verificadorVazio == 0) {
            int codigo = JDBCCliente.buscarCodigo(jFormattedTextPreencherCpfCliente.getText());
            cliente = JDBCCliente.buscarClienteDadosPessoais(codigo);

            jTextFieldNome.setText(cliente.getNome());
            jTextFieldTelefone.setText(cliente.getTelefone());
            jFormattedTextCpfCliente.setText(cliente.getCpf());
            jTextFieldCidade.setText(cliente.getCidade());
            jTextFieldBairro.setText(cliente.getBairro());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Informe o CPF!");
        }
        
    }//GEN-LAST:event_searchClienteMouseClicked

