    private void salvarPdfMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_salvarPdfMouseClicked
        try {
            if(verificarInt(jTextFieldQuantidadePeca.getText()) == false) {
                JOptionPane.showMessageDialog(rootPane, "Informe uma quantidade de peça valida!");
            }
            String data = jFormattedTextDataServico.getText();
                if(verificarData(data) == false) {
                    JOptionPane.showMessageDialog(rootPane, "Data incorreta!");
                }
            String hora = jFormattedTextHoraServico.getText();
                if(verificarHora(hora) == false) {
                    JOptionPane.showMessageDialog(rootPane, "Horario incorreto!");
                 }
            String descricao = jTextAreaDescricao.getText();
                if(verificarInt(jTextFieldValorTotal.getText()) == false) {
                     JOptionPane.showMessageDialog(rootPane, "Informe um valor da OS valido!!");
                }
            Cliente cliente = new Cliente(jTextFieldNome.getText(),jFormattedTextCpfCliente.getText(), null, null, jTextFieldTelefone.getText(), jTextFieldCidade.getText(), jTextFieldBairro.getText(), null, null, null, null);
            Funcionario funcionario = new Funcionario(jTextFieldNomeFuncionario.getText(), null, null, null, null, null, null, null, null, null, null);
            funcionario.setCd_funcionario(Integer.parseInt(jTextFieldCdFuncionario.getText()));
            Veiculo veiculo = new Veiculo(jTextFieldMarca.getText(),jTextFieldCor.getText(),jTextFieldModelo.getText(),jFormattedTextPlacaVeiculo.getText());
            Inventario inventario = new Inventario(jTextFieldMarcaPeca.getText(), Integer.parseInt(jTextFieldQuantidadePeca.getText()), jTextFieldNomePeca.getText(), jTextFieldDescricaoPeca.getText());
            inventario.setCd_inventario(Integer.parseInt(jTextFieldCodigoPeca.getText()));                
            int valorTotal = Integer.parseInt(jTextFieldValorTotal.getText());
            OrdemServico ordemServico = new OrdemServico(cliente, veiculo ,inventario, funcionario, data, hora ,valorTotal, descricao);
            gerarPdf(ordemServico);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!");
        }

    }//GEN-LAST:event_salvarPdfMouseClicked

